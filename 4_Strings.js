var s= "Damnnn brother"

//trim()
console.log(s)
s=s.trim()
console.log(s)

//toLowerCase()
console.log(s.toLowerCase())

//toUpperCase()
console.log(s.toUpperCase())

//length
console.log(s.length)

//charAt()
console.log(s.charAt(7))

//charCodeAt()
console.log(s.charCodeAt(1))

//includes()
console.log(s.includes('r'))

//indexOf()
console.log(s.indexOf('h'))

//lastIndexOf()
console.log(s.lastIndexOf('n'))

//replace()
console.log(s.replace("brother","sister"))

//replaceAll()
console.log(s.replaceAll('n','m'))

//split()
s=s.split(" ")
console.log(s)

//join()
s=s.join(' ')
console.log(s)

//substring()
console.log(s.substring(0,6))

//substr()
console.log(s.substr(0,6))